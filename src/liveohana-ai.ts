export type LiveOhanaParams = z.infer<typeof liveohanaParamsSchema>;

// ---------------------------------------------------------------------------
// Serializer
// ---------------------------------------------------------------------------

function serializeLiveOhanaUrl(
  _baseUrl: string,
  params: unknown,
): string {
  const p = params as LiveOhanaParams;
  const base = "https://liveohana.ai/search";
  const searchParams = new URLSearchParams();

  if (p.location) searchParams.set("location", p.location);
  if (p.movein) searchParams.set("movein", p.movein);
  if (p.moveout) searchParams.set("moveout", p.moveout);
  if (p.min_price != null) searchParams.set("min_price", String(p.min_price));
  if (p.max_price != null) searchParams.set("max_price", String(p.max_price));
  if (p.bedrooms) searchParams.set("bedrooms", p.bedrooms);
  if (p.property_types) searchParams.set("property_types", p.property_types);
  if (p.type_of_places) searchParams.set("type_of_places", p.type_of_places);
  if (p.amenities) searchParams.set("amenities", p.amenities);
  if (p.pet_policy) searchParams.set("pet_policy", p.pet_policy);
  if (p.furnished_status) searchParams.set("furnished_status", p.furnished_status);
  if (p.roommate) searchParams.set("roommate", p.roommate);
  if (p.max_roommates) searchParams.set("max_roommates", p.max_roommates);

  const qs = searchParams.toString();
  return qs ? `${base}?${qs}` : base;
}

