//
// Verified against the live site on 2026-05-09 via browser automation.
// LiveOhana is a mid-term rental / sublease marketplace (Bubble.io SPA).
// Focused on 1-12 month stays — the gap between Airbnb and 12-month leases.
// Strong in NYC, SF, Boston, LA. Raised ~$4M.
//
// URL format: https://liveohana.ai/search?params
//
// Params are consumed client-side by the Bubble.io SPA.
// Multi-value params use comma-separated strings.
// Values with spaces get URL-encoded; Bubble may double-encode (%2520 in URL).
//
// Full filter panel verified via browser:
//   Price (min/max), Available Bedrooms (Any/1-6), Type of Place (Any/Entire
//   home/Private room), Amenities (In-unit laundry/Building laundry/Gym/Doorman),
//   Roommate gender (Female/Male/Non-binary), Roommates (max: Any/1-5),
//   Pets (All/Pets allowed/Pets prohibited).
//   Note: Roommate gender + Roommates only visible when Type of Place = Private room.
//
// URL params confirmed by applying filters and observing URL changes:
//   location, movein, moveout, min_price, max_price, bedrooms,
//   property_types, type_of_places, amenities, pet_policy,
//   furnished_status, roommate, max_roommates.
//
// ⚠️  COLD URL HYDRATION (params that actually filter on page load):
//   ✅ Works: location, movein, moveout, min_price, max_price, property_types
//   ❌ Ignored: bedrooms, type_of_places, amenities, pet_policy,
//              furnished_status, roommate, max_roommates
//   Bubble.io reads some params on hydration but not all. The ignored params
//   only take effect when set via the internal Bubble state (UI interactions).
//   We still include all params in the schema because:
//     1. They appear in URLs generated by the site itself (shareable links)
//     2. Bubble may fix hydration for these params in the future
//     3. The serializer produces valid URLs matching what the UI generates
// ============================================================================

